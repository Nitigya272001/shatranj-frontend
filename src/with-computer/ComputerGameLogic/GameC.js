export  function onMouseoverSquare(square, bgClass, setbgClass) {
    // get list of possible moves for this square
    var moves = chess.moves({
      square: square,
      verbose: true,
    });
  
    if (moves.length === 0) return;

    greySquare(square,bgClass,setbgClass);
    for (var i = 0; i < moves.length; i++) {
      greySquare(moves[i].to,bgClass,setbgClass);
    }
  }
  
