export function handleMove(from, to, promo, colorrr) {
    // if(promo!==undefined) console.log("i am in handleMove", promo);
    const promotions = chess.moves({ verbose: true }).filter(m => m.promotion) // checking all possible moves, verbose true means we are converting it to array
    if (promotions.some(p => `${p.from}:${p.to}` === `${from}:${to}`)) {
        const pendingPromotion = { from, to, color: promotions[0].color }   //why we can't promote right away??? => we need to check first to which we promote
        updateGame(pendingPromotion)  //yeh hi check krlenga, promotion ko, aur result ko bhi
    }
    const { pendingPromotion } = gameSubject.getValue()
    console.log(pendingPromotion)

    if (!pendingPromotion || pendingPromotion.color===colorrr) {
        return move(from, to, promo)
    }
    
    return false;
}

export function move(from, to, promotion) {
    // promotion parameter is extra if you want to avoid promotion
    let tempMove = { from, to }
    if (promotion!==undefined) {
        tempMove.promotion = promotion
        console.log("i am etting promoted to ", promotion)
    }
    const legalMove = chess.move(tempMove)
    if (legalMove) {
        updateGame()
        return true;
    }

    return false;
}

export function handleMovePieces(from,to,depth,p,color) {
    //if(p!==undefined) console.log("i am in handleMovePieces", p);
    if(handleMove(from, to, p,color)) {
        //console.log("handled the move",p);
        const computerBestMove = minimaxRoot(depth, chess, true);
        if(computerBestMove!==undefined) {
            if(computerBestMove.promotion===undefined) handleMove(computerBestMove.from,computerBestMove.to)
            else handleMove(computerBestMove.from, computerBestMove.to, 'q', 'b')
        }
        else updateGame()
    }
}

